    private void iniza_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniza_btnActionPerformed
       if(this.model.isInGioco())return;
       String nome;
       int puntiMax;
       try
       {
           nome=nome_txt.getText();
           puntiMax=Integer.parseInt(puntMax_txt.getText().trim());
           this.model.iniziaPartita(nome,puntiMax);
           //puntMax_txt.setText(""+this.model.getPuntiMax());
          // nome_txt.setText(this.model.getNome());
           abilitaDisabilita(true);
           allinaModel();
       }catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,"Hai inserito un elemento non valido nei puntiMax");
       }            
    }//GEN-LAST:event_iniza_btnActionPerformed

