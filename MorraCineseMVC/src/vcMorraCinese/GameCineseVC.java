    private String prendiScelta()
    {
        if(this.model.getSceltaPc()==1) return "Sasso";
        else if(this.model.getSceltaPc()==2) return "Carta";
        else return "Forbice";
    }
    
    private void gestioneButton(java.awt.event.ActionEvent evt)
    {
        JButton b=(JButton)evt.getSource();
        //System.out.println(b.getIcon());
        if(b.getIcon()==forbici_btn.getIcon())gestioneScelta(3,"Forbici");
        else if(b.getIcon()==carta_btn.getIcon())gestioneScelta(2,"Carta");
        else gestioneScelta(1,"Sasso");
    }
    
    private void gestioneScelta(int num,String sc)
    {
        int esito=this.model.gioco(num);
        try 
        {
            Thread.sleep(1000);
            sceltaPc_lbl.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/"+this.model.getSceltaPc()+".png")));
        }
        catch(Exception e) 
        {
        }
        switch(esito)
        {
            case -9: // non siamo in gioco;
                JOptionPane.showMessageDialog(null,"Non stai giocando");
                terminaPartita();
                break;
            case -8:
                JOptionPane.showMessageDialog(null,"Ha vinto "+this.model.getNome());
                terminaPartita();
                break;
            case -7:
                JOptionPane.showMessageDialog(null,"Ha vinto il pc");
                terminaPartita();
                break;
            default:
                punti_lbl.setText(""+this.model.getPuntiUtente());
                lanci_lbl.setText(""+this.model.getLanci());
                sfida_lbl.setText(sc+" VS "+prendiScelta());
                puntiPc_lbl.setText(""+this.model.getPuntiPc());
                //System.out.println(this.model.getPuntiPc());
        } 
    }
    private void terminaPartita()
    {
        this.model.terminaPartita();
        abilitaDisabilita(false);
    }   
    private void abilitaDisabilita(boolean b) // false che non siamo in gioco
    {
        nome_txt.setEditable(!b);
        puntMax_txt.setEditable(!b);
        carta_btn.setEnabled(b);
        sasso_btn.setEnabled(b);
        forbici_btn.setEnabled(b);
        iniza_btn.setEnabled(!b);
        termina_btn.setEnabled(b);
    }
    private void allinaModel()
    {
        punti_lbl.setText("0");
        lanci_lbl.setText("0");
        sfida_lbl.setText("? VS ?");
        puntiPc_lbl.setText("0");
        sceltaPc_lbl.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/start.png")));
    }
